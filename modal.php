      </div>
      <div class="modal-body">
        <p class="lead" style=" text-indent: 50px;">The <strong> COLLEGE OF ENGINEERING AND INFORMATION TECHNOLOGY (CEIT) </strong>started as the Department of Agricultural Engineering in <i>1976</i>. It offered basic agricultural engineering courses required by the BSA curriculum.
</p>
 <p class="lead" style=" text-indent: 50px;">
In <i>1985</i>, the unit was renamed to <strong> Department of Engineering and Agro-Industrial Technology (DEAIT)</strong>. By virtue of Board Resolution No. 5, S1985, it started to offer Bachelor of Science in Agricultural Engineering on the first semester of SY <i>1985 - 1986</i>.
</p>
 <p class="lead" style=" text-indent: 50px;">
In <i>1992</i>, the department was elevated to the School status. This resulted in the creation of two (2) distinct departments: the Engineering Science Department and the Agricultural Engineering Department. The formation of these departments delineated the basic engineering science courses from the agricultural engineering courses.
</p>
 <p class="lead" style=" text-indent: 50px;">
In <i>1993</i>, the Department of Engineering Technology was created. The newly created department offers non-degree courses in engineering technology.
</p>
 <p class="lead" style=" text-indent: 50px;">
In <i>1998</i>, when the College was elevated to University status, the <strong> School of Engineering (SEng)</strong> was subsequently elevated to the College level and was named as the <strong>College of Engineering (CEng)</strong>.
</p>
 <p class="lead" style=" text-indent: 50px;">
To accommodate IT courses offerings; the College was renamed as the <strong>College of Engineering and Information Technology (CEIT)</strong> in <i>2001</i>.
</p>
 <p class="lead" style=" text-indent: 50px;">
 As approved by the Board of Regents, <strong>CEIT</strong> offers various engineering programs, computer-based programs, and industrial technology programs
 </p>
